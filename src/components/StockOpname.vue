<script setup>
import { ref, onMounted, watch } from "vue"
import { useRouter } from "vue-router"
import { useUserStore } from "@/stores/user"
import api from "../axios"
import TopNavbar from "./reusable/TopNavbar.vue"
import Input from "./ui/input/Input.vue"
import Button from "./ui/button/Button.vue"
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select"

const router = useRouter()
const userStore = useUserStore()

// State
const brands = ref([])
const selectedBrand = ref("")
const products = ref([])
const loading = ref(false)
const physicalStocks = ref({})
const verified = ref(false)
const differences = ref({})
const adjustMode = ref({})

// Fetch daftar brand
const fetchBrands = async () => {
  try {
    const res = await api.get(`/brand?storeId=${userStore.storeId}`)
    brands.value = res.data.map((b) => ({
      label: b.name,
      value: String(b.id),
    }))
  } catch (err) {
    console.error("Gagal ambil brand:", err)
  }
}

// Fetch produk berdasarkan brand
const fetchProducts = async () => {
  if (!selectedBrand.value) return
  loading.value = true
  try {
    const res = await api.get(`/products?brandId=${selectedBrand.value}`)
    products.value = res.data.data || []
    physicalStocks.value = {}
    verified.value = false
    differences.value = {}
    adjustMode.value = {}
  } catch (err) {
    console.error("Error fetching products:", err)
  } finally {
    loading.value = false
  }
}

// Proses verifikasi stok
const verifyStocks = () => {
  verified.value = true
  products.value.forEach((p) => {
    const physical = Number(physicalStocks.value[p.id] || 0)
    const system = Number(p.stok)
    const diff = physical - system
    differences.value[p.id] = diff

    // jika ada selisih maka input aktif & tombol adjust aktif
    adjustMode.value[p.id] = diff !== 0
  })
}

// Toggle mode Adjust
const toggleAdjust = (productId) => {
  // Jika sedang dalam mode adjust (klik selesai)
  if (adjustMode.value[productId]) {
    adjustMode.value[productId] = false
    verified.value = false // aktifkan kembali tombol verifikasi
  } else {
    // Jika bukan dalam mode adjust, aktifkan kembali input
    adjustMode.value[productId] = true
  }
}

// Kirim hasil opname
const submitStockOpname = async () => {
  if (!verified.value) {
    alert("Silakan lakukan verifikasi terlebih dahulu!")
    return
  }

  const opnameItems = products.value.map((p) => ({
    productId: p.id,
    systemStock: p.stok,
    physicalStock: Number(physicalStocks.value[p.id] || 0),
    difference: differences.value[p.id] || 0,
  }))

  try {
    await api.post("/stock-opname", {
      brandId: selectedBrand.value,
      storeId: userStore.storeId,
      items: opnameItems,
      note: "Stock opname otomatis",
    })
    alert("Data stok opname berhasil disimpan!")
    products.value = []
    selectedBrand.value = ""
    verified.value = false
    adjustMode.value = {}
  } catch (err) {
    console.error("Gagal menyimpan:", err)
    alert("Gagal menyimpan stok opname.")
  }
}

onMounted(() => {
  fetchBrands()
})
</script>

<template>
  <TopNavbar title="Stok Opname" />

  <div class="flex flex-col gap-3 items-start lg:justify-center mb-10 pt-5 px-5">
    <h1 class="text-2xl font-bold mb-3">Stok Opname</h1>

    <!-- Pilih Brand -->
    <div class="flex flex-col gap-2 w-full max-w-sm">
      <label class="text-sm font-semibold text-gray-700">Pilih Brand</label>

      <Select v-model="selectedBrand">
        <SelectTrigger class="w-full">
          <SelectValue placeholder="Pilih brand" />
        </SelectTrigger>
        <SelectContent>
          <SelectItem v-for="b in brands" :key="b.value" :value="b.value">
            {{ b.label }}
          </SelectItem>
        </SelectContent>
      </Select>

      <Button
        @click="fetchProducts"
        :disabled="!selectedBrand || loading"
        class="mt-2 w-fit"
      >
        {{ loading ? "Memuat..." : "Cari Data" }}
      </Button>
    </div>

    <!-- Tabel Produk -->
    <div v-if="products.length" class="w-full mt-6 overflow-x-auto">
      <table class="min-w-full border border-gray-200 rounded-lg">
        <thead class="bg-gray-100">
          <tr>
            <th class="px-4 py-2 text-left text-sm font-semibold">Nama Produk</th>
            <th class="px-4 py-2 text-left text-sm font-semibold">Stok Fisik</th>
            <th class="px-4 py-2 text-left text-sm font-semibold">Selisih</th>
            <th class="px-4 py-2 text-left text-sm font-semibold">Aksi</th>
          </tr>
        </thead>
        <tbody>
          <tr
            v-for="product in products"
            :key="product.id"
            class="border-t border-gray-200"
          >
            <td class="px-4 py-2">{{ product.name }}</td>
            <td class="px-4 py-2">
              <Input
                type="number"
                min="0"
                class="w-24"
                v-model="physicalStocks[product.id]"
                :disabled="verified && !adjustMode[product.id]"
              />
            </td>
            <td class="px-4 py-2 text-sm">
              <span
                v-if="verified"
                :class="{
                  'text-green-600': differences[product.id] > 0,
                  'text-red-600': differences[product.id] < 0,
                  'text-gray-600': differences[product.id] === 0,
                }"
              >
                {{ differences[product.id] }}
              </span>
              <span v-else class="text-gray-400 italic">-</span>
            </td>
            <td class="px-4 py-2">
              <Button
                variant="secondary"
                size="sm"
                @click="toggleAdjust(product.id)"
                :disabled="!verified || differences[product.id] === 0"
              >
                {{
                  adjustMode[product.id]
                    ? "Selesai"
                    : "Adjust"
                }}
              </Button>
            </td>
          </tr>
        </tbody>
      </table>
    </div>

    <div v-else-if="!loading && selectedBrand" class="text-gray-500 mt-6">
      Tidak ada produk untuk brand ini.
    </div>

    <!-- Tombol Verifikasi & Simpan -->
    <div
      v-if="products.length"
      class="flex flex-col sm:flex-row gap-3 justify-end mt-6 w-full"
    >
      <Button
        variant="outline"
        @click="verifyStocks"
        :disabled="verified && !Object.values(adjustMode).includes(true)"
        class="px-6"
      >
        {{ verified ? "Terverifikasi" : "Verifikasi" }}
      </Button>
      <Button @click="submitStockOpname" class="px-6" :disabled="!verified">
        Simpan
      </Button>
    </div>
  </div>
</template>
